package org.neo4japps.webgraph.util;

public final class StringFormatUtil {
    public static String formatNodesPerSecond(double value) {
        return String.format("%4.2f", value);
    }

    public static String formatSeconds(double value) {
        return String.format("%4.2f", value);
    }

    public static String formatMinutes(double value) {
        return String.format("%4.2f", value);
    }
}
